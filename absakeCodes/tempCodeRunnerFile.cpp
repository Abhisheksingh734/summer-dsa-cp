void absake()
{
    ll n, k;
    cin >> n >> k;

    vector<ll> arr;

    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    unordered_map<ll, ll> mp;

    for (auto &&x : arr)
    {
        mp[x]++;
    }

    vector<ll> freq;
    for (auto &&it : mp)
    {
        freq.push_back(it.second);
    }

    sort(freq.begin(), freq.end());

    ll cnt = 0;
    while (k > 0 && cnt < freq.size() - 1)
    {

        if (k >= freq[cnt])
        {
            k -= freq[cnt];
            cnt++;
        }
        else
        {
            break;
        }
    }

    cout << freq.size() - cnt << endl;
}


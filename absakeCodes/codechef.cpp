#include <bits/stdc++.h>
using namespace std;
#define ll long long

bool canKill(ll A, int N, ll X, const vector<ll> &H)
{
    for (int i = 0; i < N; ++i)
    {
        ll hp = H[i] + i * X;
        if (A < hp)
        {
            return false;
        }
    }
    return true;
}

ll absake(int N, ll X, vector<ll> &H)
{
    sort(H.begin(), H.end());
    ll s = 1;
    ll e = H[N - 1] + (ll)(N - 1) * X;
    ll res = e;

    sort(H.rbegin(), H.rend());
    while (s <= e)
    {
        ll mid = s + (e - s) / 2;
        if (canKill(mid, N, X, H))
        {
            res = mid;
            e = mid - 1;
        }
        else
        {
            s = mid + 1;
        }
    }

    return res;
}


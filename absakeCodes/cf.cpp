void absake()
{
    int n;
    cin >> n; // Input size
    vector<int> v;
    v.pb(2);
    int cnt = 1;

    for (int i = 2; i < n; i++)
    {
        int sum = cnt + i;
        v.pb(sum); // Push values dynamically
        cnt++;
    }

    for (auto x : v)
    {
        cout << x << " ";
    }
    cout << endl;
}





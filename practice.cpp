int bsearch_l(vector<char> &arr, int x)
{
    int l = -1;         // arr[l] <= x
    int r = arr.size(); // arr[r] > x

    while (r > l + 1)
    {
        int m = l + (r - l) / 2;

        if (arr[m] <= x)
        {
            l = m;
        }
        else
        {
            r = m;
        }
    }

    return l;
}

int main()
{

    int arr[5] = {1, 2, 3, 4, 5};
    int target = 4;

    int start = 0;
    int end = 4;

    while (start <= end)
    {
        int mid = start + (end - start) / 2;

        if (arr[mid] == target)
        {
            cout << mid;
            return 0;
        }
        else if (arr[mid] < target)
        {
            start = mid + 1;
        }
        else
        {
            end = mid - 1;
        }
    }

    cout << "Not found";
}

